import Foundation

/// Enumerado que representa los diferentes tipos de
/// formato que puede tener el texto de una nota.
enum TextFormat {
    case bold
    case italic
    case underline
    case strikethrough
    case title
    case header
    case subtitle
    case body
    case bulletlist
    case dashList
    case numberedList
}
